    /**
     * Con este metodo conseguimos asignar el Prize a la vista para mostrar
     * todos sus atributos.
     *
     * @param aPrice es un objeto Prize que lo pedimos al controlador.
     */
    protected void setPrize(Prize aPrice) {
        this.prizeModel = aPrice;
        int tesoros = this.prizeModel.getTreasures(), niveles = this.prizeModel.getLevels();
        switch (tesoros) {
            case 0:
                this.jLTesoros.setText("No Ganas Tesoros");
                break;
            case 1:
                this.jLTesoros.setText("Puedes Ganar: " + tesoros + " Tesoro");
                break;
            default:
                this.jLTesoros.setText("Puedes Ganar: " + tesoros + " Tesoros");
                break;
        }
        switch (niveles) {
            case 0:
                this.jLNiveles.setText("No Ganas Niveles");
                break;
            case 1:
                this.jLNiveles.setText("Puedes Ganar: " + niveles + " Nivel");
                break;
            default:
                this.jLNiveles.setText("Puedes Ganar: " + niveles + " Niveles");
                break;
        }
        repaint();
        revalidate();
    }

