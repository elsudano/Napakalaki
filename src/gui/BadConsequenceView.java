    /**
     * Con este metodo conseguimos asignar el BadConsequence a la vista para
     * mostrar todos sus atributos.
     *
     * @param aBadConsequence es un objeto de cualquiera de las clases que hay
     * de BadConsequence, ya que lo leera igualmente.
     */
    protected void setBadConsequence(BadConsequence aBadConsequence) {
        this.badconsequenceModel = aBadConsequence;
        this.jTATextoMalRollo.setText(this.badconsequenceModel.getText());
        this.jTNiveles.setText("" + this.badconsequenceModel.getLevels());
        if (this.badconsequenceModel.getDeath()) {
            this.jTMuerte.setText("Si");
        } else {
            this.jTMuerte.setText("No");
        }
        if (this.badconsequenceModel instanceof NumericBadConsequence) {
            this.jTATesorosVisibles.setText(""+((NumericBadConsequence) this.badconsequenceModel).getNVisibleTreasures());
            this.jTATesorosOcultos.setText(""+((NumericBadConsequence) this.badconsequenceModel).getNHiddenTreasures());
        } else if (this.badconsequenceModel instanceof SpecificBadConsequence) {
            this.jTATesorosVisibles.setText(((SpecificBadConsequence) this.badconsequenceModel).getSpecificVisibleTreasures().toString());
            this.jTATesorosOcultos.setText(((SpecificBadConsequence) this.badconsequenceModel).getSpecificHiddenTreasures().toString());
        } else {
            this.jLPierdesTesorosVisibles.setVisible(false);
            this.jLPierdesTesorosOcultos.setVisible(false);
            this.jTATesorosVisibles.setVisible(false);
            this.jTATesorosOcultos.setVisible(false);
        }
        repaint();
        revalidate();
    }

